package com.example.phuongnamlib.Models;

public class BorrowedKind {
    String name;
    int count;

    public BorrowedKind(String name, int count) {
        this.name = name;
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
